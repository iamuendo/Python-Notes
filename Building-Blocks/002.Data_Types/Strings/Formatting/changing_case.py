name = input("Name: ")
message = "Hello, my name is "

# Using the title() method: Titlecase
# Titlecase is where each word begins with a capital letter
print("Title case: " + message + name.title())

# Using the upper() method: Uppercase
print("Upper case: " + message + name.upper())

# Using the lower() method: Lowercase
print("Lower case: " + message + name.lower())